import turtle
def draw_graphics():
    turtle.bgcolor("black")
    brad=turtle.Turtle()
    i=0
    brad.color("yellow")
    while(i<4):
        brad.circle(100)
        brad.circle(80)
        brad.circle(60)
        brad.circle(40)
        brad.circle(20)
        brad.left(90)
        i=i+1
            
        
        
draw_graphics()
   
